def insert_paragraph_after(paragraph: Paragraph, text: str) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_para = Paragraph(new_p, paragraph._parent)
    new_para.style = paragraph.style
    new_para.add_run(text)
    return new_para


def find_paragraph(doc: Document, exact_text: str) -> Paragraph | None:
    matched: Paragraph | None = None
    for paragraph in doc.paragraphs:
        if paragraph.text.strip() == exact_text:
            matched = paragraph
    return matched


def add_after(doc: Document, anchor_text: str, paragraphs: list[str]) -> None:
    anchor = find_paragraph(doc, anchor_text)
    if anchor is None:
        raise ValueError(f"未找到锚点段落: {anchor_text}")
    current = anchor
    for text in paragraphs:
        current = insert_paragraph_after(current, text)


