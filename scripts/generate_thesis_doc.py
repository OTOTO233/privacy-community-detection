def append_caption(document: Document, text: str) -> None:
    append_paragraph(document, text, style="Caption", align=WD_ALIGN_PARAGRAPH.CENTER)


def append_table(document: Document, title: str, headers: list[str], rows: list[list[str]]) -> None:
    append_caption(document, title)
    table = document.add_table(rows=1, cols=len(headers))
    try:
        table.style = "Table Grid"
    except KeyError:
        pass
    for idx, header in enumerate(headers):
        table.rows[0].cells[idx].text = header
    for row in rows:
        cells = table.add_row().cells
        for idx, value in enumerate(row):
            cells[idx].text = value


def append_picture(document: Document, path: Path, caption: str, width_cm: float = 14.5) -> None:
    if not path.exists():
        return
    paragraph = document.add_paragraph()
    paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
    paragraph.add_run().add_picture(str(path), width=Cm(width_cm))
    append_caption(document, caption)


def set_cell_border(cell, **kwargs) -> None:
    tc = cell._tc
    tc_pr = tc.get_or_add_tcPr()
    tc_borders = tc_pr.first_child_found_in("w:tcBorders")
    if tc_borders is None:
        tc_borders = OxmlElement("w:tcBorders")
        tc_pr.append(tc_borders)
    for edge in ("top", "left", "bottom", "right"):
        edge_data = kwargs.get(edge)
        if edge_data is None:
            continue
        tag = f"w:{edge}"
        element = tc_borders.find(qn(tag))
        if element is None:
            element = OxmlElement(tag)
            tc_borders.append(element)
        for key, value in edge_data.items():
            element.set(qn(f"w:{key}"), str(value))


def add_algorithm_run(paragraph, text: str) -> None:
    keyword_pattern = re.compile(r"\b(for|if|end if|end for|do|then|else|return)\b", re.IGNORECASE)
    pos = 0
    for match in keyword_pattern.finditer(text):
        if match.start() > pos:
            paragraph.add_run(text[pos : match.start()])
        run = paragraph.add_run(match.group(0))
        run.bold = True
        pos = match.end()
    if pos < len(text):
        paragraph.add_run(text[pos:])


def normalize_algorithm_title(title: str) -> str:
    return re.sub(r"^算法(\S+)\s*", r"算法 \1  ", title)


def normalize_algorithm_line(index: int, line: str) -> str:
    stripped = line.strip()
    if re.match(r"^\d+\.", stripped):
        return re.sub(r"^(\d+)\.", rf"{index}:", stripped, count=1)
    return f"{index}: {stripped}"


def append_algorithm(document: Document, title: str, lines: list[str]) -> None:
    table = document.add_table(rows=1, cols=1)
    cell = table.cell(0, 0)
    set_cell_border(
        cell,
        top={"val": "single", "sz": "12", "color": "000000"},
        bottom={"val": "single", "sz": "12", "color": "000000"},
        left={"val": "single", "sz": "4", "color": "BFBFBF"},
        right={"val": "single", "sz": "4", "color": "BFBFBF"},
    )

    header = cell.paragraphs[0]
    header.alignment = WD_ALIGN_PARAGRAPH.LEFT
    header_run = header.add_run(normalize_algorithm_title(title))
    header_run.bold = True

    for idx, line in enumerate(lines, start=1):
        paragraph = cell.add_paragraph()
        paragraph.paragraph_format.left_indent = Cm(0.15)
        add_algorithm_run(paragraph, normalize_algorithm_line(idx, line))


